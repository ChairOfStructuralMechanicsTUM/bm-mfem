% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
fig = findobj('Tag','figure1');
data = guidata(fig);
model = data.model;
elements = model.getAllElements();
youngsModulus = str2num(get(handles.edit1,'string'));
thickness = str2num(get(handles.edit2,'string'));
prxy = str2num(get(handles.edit3,'string'));
nrGaussPoint = str2num(get(handles.edit4,'string'));
density = str2num(get(handles.edit5,'string'));

elements.setPropertyValue('THICKNESS', thickness);
elements.setPropertyValue('YOUNGS_MODULUS', youngsModulus);
elements.setPropertyValue('POISSON_RATIO', prxy);
elements.setPropertyValue('NUMBER_GAUSS_POINT', nrGaussPoint);
elements.setPropertyValue('DENSITY', density);
set(findall(data.uipanel_bC, '-property', 'enable'), 'enable', 'on');

close(gcf);

