% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
fig = findobj('Tag','figure1');
data = guidata(fig);
model = data.model;
nodes = model.getAllNodes();
dof = zeros(length(nodes),2);
for i = 1:length(nodes)
    dof(i,1) = nodes(i).getDof('DISPLACEMENT_X').isFixed();
    dof(i,2) = nodes(i).getDof('DISPLACEMENT_Y').isFixed();
end
constrained = any(dof(:));
data.constrained = constrained;

figure(fig);
data.vis.plotConstrain;
guidata(fig,data);

try
    loaded = data.loaded;
catch
    loaded = false;
end

if constrained
    close(handles.figure_ConstrainBC);
    if loaded
        set(data.pushbutton_solve, 'enable', 'on');
    end
else
    warndlg('No Boundary Conditions applied.','Warning');
end
